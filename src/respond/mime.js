const path=require('path')

const mimeTypes={
  "css": "text/css",
  "gif": "image/gif",
  "html": "text/html",
  "ico": "image/x-icon",
  "jpeg": "image/jpeg",
  "jpg": "image/jpeg",
  "js": "text/javascript",
  "json": "application/json",
  "pdf": "application/pdf",
  "png": "image/png",
  "svg": "image/svg+xml",
  "swf": "application/x-shockwave-flash",
  "tiff": "image/tiff",
  "txt": "text/plain",
  "wav": "audio/x-wav",
  "wma": "audio/x-ms-wma",
  "wmv": "video/x-ms-wmv",
  "xml": "text/xml",
}

const mime=(pathName)=>{
  let ext=path.extname(pathName)
  ext=ext ? ext.slice(1) : 'unknown'
  return mimeTypes[ext] ? mimeTypes[ext] : mimeTypes['txt']
}

module.exports=mime